#include <iostream>

using namespace std;

/**
 * @brief Simple program demonstrating variable usage and output
 * @return int Exit status code
 */
int main() {
    // Initialize variables with descriptive names
    int firstNumber = 10;   // Previously 'tom'
    int secondNumber = 20;  // Previously 'richard'
    int thirdNumber = 3254; // Previously 'charles'

    // Output the numbers with descriptive labels
    cout << "First number: " << firstNumber << endl;
    cout << "Second number: " << secondNumber << endl;
    cout << "Third number: " << thirdNumber << endl;

    return 0;
}
